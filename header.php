<script>
   
 $(document).ready(function(){

	$(document).click(function(e) { 
		
		$(window).resize(function(){
	    $("#m-nav-menu").hide();
	    $("#close-button").hide();
	    $('#m-nav-menu').removeClass("active");
	    $('#m-nav-menu').addClass("inactive");
	    if($(window).width()> 767)
	    {    	
	    	$("#gl-mobile-menu-button").hide();
	    }
			else
			{
				$("#gl-mobile-menu-button").show();
			}
		});		
		
    if(!$(e.target).closest('#gl-mobile-menu-button').length && !$(e.target).closest('#m-nav-menu').length) {
			if ($('#m-nav-menu').hasClass("active"))
			{
				$('#m-nav-menu').removeClass("active");
		    $('#m-nav-menu').addClass("inactive");
		    $('#m-nav-menu').animate({width:'toggle'},300);
				$("#gl-mobile-menu-button").css('display', 'inline');
				$(".top-menu").hide();
				$("#l1-menu").hide();
				$("#close-button").hide();
			}
    }        
	})

	$("#gl-mobile-menu-button").click(function(){
		if ($('#m-nav-menu').hasClass("inactive"))
		{
			$('#m-nav-menu').removeClass("inactive");
	    $('#m-nav-menu').addClass("active");
	    $('#m-nav-menu').animate({width:'toggle'},300);
			$("#close-button").css('display', 'inline');
			$("#gl-mobile-menu-button").hide();
		}				
	});
	
	$(".m-button").click(function(){
		var myMenu = '#' + this.id + "-menu";
    if ($(myMenu).hasClass("inactive"))
    {
    	if ($(myMenu).hasClass("top-menu"))
    	{
    		$(".l1-menu").slideUp("fast");
    		$(".l1-menu").removeClass("active");
	    	$(".l1-menu").addClass("inactive");
	    	$(".top-menu").slideUp("fast");
	    	$(".top-menu").removeClass("active");
	    	$(".top-menu").addClass("inactive");
	    	$(myMenu).slideDown("fast");
	    	$(myMenu).removeClass("inactive");
	    	$(myMenu).addClass("active");
	    }
	    else
	    {
	    	$(".l1-menu").slideUp("fast");
	    	$(".l1-menu").removeClass("active");
	    	$(".l1-menu").addClass("inactive");
	    	$(myMenu).slideDown("fast");
	    	$(myMenu).removeClass("inactive");
	    	$(myMenu).addClass("active");
	    }
    }
		else
		{
			if ($(myMenu).hasClass("top-menu"))
    	{
    		$(".l1-menu").slideUp("fast");
     		$(".l1-menu").removeClass("active");   	
	    	$(".l1-menu").addClass("inactive");
	    	$(".top-menu").slideUp("fast");
    		$(".top-menu").removeClass("active");   	
	    	$(".top-menu").addClass("inactive");
	    }
	    else
	    {
	    	$(".l1-menu").slideUp("fast");
    		$(".l1-menu").removeClass("active");  	
	    	$(".l1-menu").addClass("inactive");
	    }
		}
	});
});
	
</script>